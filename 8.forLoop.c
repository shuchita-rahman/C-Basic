#include <stdio.h>
#include <string.h>

int main()
{
   //for loop variable increasing
   printf("Ascending Order : ");
   for(int i = 1; i <= 10; i++)
   {
       printf("\n%d", i);
   }

  //for loop variable increasing
   printf("\nDescending Order : ");
   for(int i = 10; i >= 1; i--)
    {
       printf("\n%d", i);
   }


    return 0;
}
